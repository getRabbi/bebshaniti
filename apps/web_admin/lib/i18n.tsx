export type TranslationKey = keyof typeof messages.en;

export function storedLocale(): Locale {
  if (typeof window === "undefined") return "bn-BD";
  return window.localStorage.getItem("business-os-locale") === "en"
    ? "en"
    : "bn-BD";
}

export function translate(key: TranslationKey, locale = storedLocale()) {
  return messages[locale][key];
}
const I18nContext = createContext<{
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey) => string;
} | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("bn-BD");
  useEffect(() => {
    const stored = window.localStorage.getItem("business-os-locale");
    if (stored === "en" || stored === "bn-BD") setLocaleState(stored);
  }, []);
  useEffect(() => {
    document.documentElement.lang = locale;
    document.documentElement.dir = "ltr";
  }, [locale]);
  const value = useMemo(
    () => ({
      locale,
      setLocale: (next: Locale) => {
        setLocaleState(next);
        window.localStorage.setItem("business-os-locale", next);
      },
      t: (key: TranslationKey) => messages[locale][key],
    }),
    [locale],
  );
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) throw new Error("I18nProvider is missing");
  return value;
}
